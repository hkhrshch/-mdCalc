public class ArithmeticOperation {



    protected int add(int x, int y) {

        int result = x + y;
        return result;
    }

    protected int substract(int x, int y) {

        int result = x - y;
        return result;
    }

    protected int multiply(int x, int y) {

        int result = x * y;
        return result;
    }

    protected int divide(int x, int y) {
		int result = x / y;
        return result;     
    }
}
